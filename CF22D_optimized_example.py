HF_COEFF = 0.462806


def _compact_key(key):
    return "".join(key.split()).upper()


def _parse_float(raw):
    return float(raw.replace("D", "E").replace("d", "e"))


def read_cf22d_parameters(path=DEFAULT_PARAMETERS_PATH):
    """Read CF22D parameters from a DFThub `Parameters` file."""
    path = Path(path)
    values = {}
    hf_coeff = None

    for line in path.read_text().splitlines():
        line = line.split("#", 1)[0].strip()
        if not line or "=" not in line:
            continue
        key, raw = [part.strip() for part in line.split("=", 1)]
        key = _compact_key(key)

        if key == "HF%":
            hf_coeff = _parse_float(raw) / 100.0
        elif key == "HF":
            hf_coeff = _parse_float(raw)
        elif key in CX_KEYS or key in CY_KEYS or key in CZ_KEYS:
            values[key] = _parse_float(raw)

    missing = [key for key in CX_KEYS + CY_KEYS + CZ_KEYS if key not in values]
    if missing or hf_coeff is None:
        missing_msg = ", ".join(missing[:5])
        if len(missing) > 5:
            missing_msg += ", ..."
        raise ValueError(f"Incomplete Parameters file {path}: missing {missing_msg or 'HF'}")

    cx = np.array([values[key] for key in CX_KEYS])
    cy = np.array([values[key] for key in CY_KEYS])
    cz = np.array([values[key] for key in CZ_KEYS])
    return cx, cy, cz, hf_coeff


def _get_parameters(parameters_path):
    if parameters_path is None:
        return CX, CY, CZ, HF_COEFF
    return read_cf22d_parameters(parameters_path)


def register_cf22d(mf, name="cf22d_custom", parameters_path=DEFAULT_PARAMETERS_PATH, with_d3=True):
    """Register the CF22D functional and apply it to *mf*.

    Parameters
    ----------
    mf : pyscf.dft.rks.RKS or pyscf.dft.uks.UKS
        A PySCF mean-field object.
    name : str, optional
        Internal name for the custom functional (default: "cf22d_custom").
    parameters_path : str or pathlib.Path or None, optional
        DFThub `Parameters` file. Use None to keep the embedded parameters.
    with_d3 : bool, optional
        Enable CF22D D3 zero-damping dispersion through PySCF.

    Returns
    -------
    mf : the same mean-field object with xc set to the CF22D functional.
    """
    cx, cy, cz, hf_coeff = _get_parameters(parameters_path)

    x_params = np.zeros(41)
    x_params[:40] = cx
    x_params[40] = hf_coeff

    c_params = np.zeros(24)
    c_params[:9] = cy
    c_params[12:21] = cz

    name = name.lower()
    libxc.register_custom_functional_(
        name,
        "CF22D,CF22D",
        ext_params={340: x_params, 341: c_params},
    )
    mf.xc = name
    mf.disp = CF22D_D3_DISPERSION if with_d3 else False
    return mf


